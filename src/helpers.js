// colors
const generateRandomColor = () => {
  const existingBudgetLength = fetchData("budgets")?.length ?? 0;
  return `${existingBudgetLength * 34} 65% 50%`;
};

// Local storage
// This function retrieves data from local storage based on the provided key. 
// It parses the JSON data stored in the local storage and returns it.
export const fetchData = (key) => {
  return JSON.parse(localStorage.getItem(key));
};

// Get all items from local storage
// This function retrieves all items from local storage in the specified category that match a specific key-value pair. It filters the data array and returns items that have a matching value for the specified key.
export const getAllMatchingItems = ({ category, key, value }) => {
  const data = fetchData(category) ?? [];
  return data.filter((item) => item[key] === value);
};

// delete item from local storag
export const deleteItem = ({ key, id }) => {
  const existingData = fetchData(key);
  if (id) {
    const newData = existingData.filter((item) => item.id !== id);
    return localStorage.setItem(key, JSON.stringify(newData));
  }
  return localStorage.removeItem(key);
};

// create budget
export const createBudget = ({ name, amount }) => {
  const newItem = {
    id: crypto.randomUUID(),
    name: name,
    createdAt: Date.now(),
    amount: +amount,
    color: generateRandomColor(),
  };
  const existingBudgets = fetchData("budgets") ?? [];
  return localStorage.setItem(
    "budgets",
    JSON.stringify([...existingBudgets, newItem])
  );
};

// create expense
export const createExpense = ({ name, amount, budgetId }) => {
  const newItem = {
    id: crypto.randomUUID(),
    name: name,
    createdAt: Date.now(),
    amount: +amount,
    budgetId: budgetId,
  };
  const existingExpenses = fetchData("expenses") ?? [];
  return localStorage.setItem(
    "expenses",
    JSON.stringify([...existingExpenses, newItem])
  );
};

// total spent by budget
export const calculateSpentByBudget = (budgetId) => {
  const expenses = fetchData("expenses") ?? [];
  const budgetSpent = expenses.reduce((acc, expense) => {
    // check if expense.id === budgetId I passed in
    if (expense.budgetId !== budgetId) return acc;

    // add the current amount to my total
    return (acc += expense.amount);
  }, 0);
  return budgetSpent;
};

// FORMATTING
export const formatDateToLocaleString = (epoch) =>
  new Date(epoch).toLocaleDateString();

// Formating percentages
export const formatPercentage = (amt) => {
  return amt.toLocaleString(undefined, {
    style: "percent",
    minimumFractionDigits: 0,
  });
};

// Format currency
export const formatCurrency = (amt) => {
  return amt.toLocaleString(undefined, {
    style: "currency",
    currency: "INR",
  });
};

// Update a budget in local storage
export const updateBudget = (updatedBudget) => {
  const existingBudgets = fetchData('budgets') ?? [];
  const updatedBudgets = existingBudgets.map((budget) =>
    budget.id === updatedBudget.id ? updatedBudget : budget
  );
  localStorage.setItem('budgets', JSON.stringify(updatedBudgets));
};

// Update an expense
export const updateExpense = (expenseId, updatedExpense) => {
  const expenses = fetchData("expenses") ?? [];
  const updatedExpenses = expenses.map((expense) =>
    expense.id === expenseId ? updatedExpense : expense
  );
  localStorage.setItem("expenses", JSON.stringify(updatedExpenses));
};

// delete expense
export const deleteExpense = (expenseId) => {
  const expenses = fetchData('expenses') || [];

  const updatedExpenses = expenses.filter((expense) => expense.id !== expenseId);

  localStorage.setItem('expenses', JSON.stringify(updatedExpenses));
};